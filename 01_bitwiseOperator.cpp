#include<iostream>
using namespace std;

int main () {
    int a = 4;
    int b = 6;

    // bitwise operators
    // AND
    // OR
    // NOT
    // XOR
    cout<< "AND Operators " << (a&b) << endl;
    cout<< "OR Operators " << (a|b) << endl;
    cout<< "NOT Operators " << ~a << endl;
    cout<< "XOR Operators " << (a^b) << endl;

    int c = 2;
    cout<< ~c << endl;
    cout << true << endl;
    cout << false << endl;
}